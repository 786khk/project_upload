package model1;

public class materialTO {
	
	private String mtseq;	
	private int rcseq;	
	private String name;
	private String amount;
	private String id;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMtseq() {
		return mtseq;
	}
	public void setMtseq(String mtseq) {
		this.mtseq = mtseq;
	}
	
	public int getRcseq() {
		return rcseq;
	}
	public void setRcseq(int rcseq) {
		this.rcseq = rcseq;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	

			
}
